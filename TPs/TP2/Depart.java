public class Depart {
    private final int a,b,c;

    public Depart(int a, int b , int c){
        this.a=a;
        this.b=c;
        this.c=c;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public int getC() {
        return c;
    }
}